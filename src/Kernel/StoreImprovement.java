package Kernel;

public class StoreImprovement {
	private long improveRange;
	private long improveDamage;
	private long improveAttackSpeed;
	public long getImproveRange() {
		return improveRange;
	}
	public void setImproveRange(long improveRange) {
		this.improveRange = improveRange;
	}
	public long getImproveDamage() {
		return improveDamage;
	}
	public void setImproveDamage(long improveDamage) {
		this.improveDamage = improveDamage;
	}
	public long getImproveAttackSpeed() {
		return improveAttackSpeed;
	}
	public void setImproveAttackSpeed(long improveAttackSpeed) {
		this.improveAttackSpeed = improveAttackSpeed;
	}
	
}
